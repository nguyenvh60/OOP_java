package Bai10_Interface;

// Cat "triển khai" interface Animal
public class Cat implements Animal {
    public void eat() {
        // Nội dung của eat() được cung cấp ở đây
        System.out.println("Ăn cá");
    }
    public void sleep() {
        // Nội dung của sleep () được cung cấp ở đây
        System.out.println("Zzz");
    }

}
